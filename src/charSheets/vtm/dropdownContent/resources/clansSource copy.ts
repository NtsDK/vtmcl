// С ообщество каинитов, как и каиниты в целом, существует за счёт разности потенциалов.
// В Мире Тёмного средневековья она принимает облик явления, называющегося Высшими и Низшими
// кланами. В конечном итоге от региона или города
// зависит, какие из живущих в нём кланов относятся
// к высшему свету и аристократии, а какие нет

// любая линия крови, не хвастающаяся
// на каждом углу происхождением от Высшего клана,
// считается Низшим кланом или даже, в зависимости
// от местных традиций, занимает ещё более низкое
// положение. Исключений из этого правила от ночи
// к ночи становится всё меньше, что не предвещает
